        echo "  --auto    Run migration without prompts"
        echo "  --help    Show this help message"
        echo
        echo "This script migrates existing RLAMA installations to use"
        echo "enhanced document processing with LangChain integration."
        exit 0
        ;;
    *)
        main "$@"
        ;;
esac